# Tier 3 — M* and σ_SFR separation
# ---------------------------------------------------------------------------

class TestMassSigmaDecomposition:
    """
    Verify that a_M (mass dependence) and c_sigma (σ_SFR dependence of T_w)
    are separately recoverable when both vary.

    If posterior contours are elongated along a_M + c_sigma = const, a
    degeneracy exists and the simulator exposes it at COSMOS noise levels.
    """

    def test_sigma_ordering_preserved(self, dem):
        """T_w(high σ_SFR) > T_w(low σ_SFR) — recovered by the fitter."""
        sim = dem.simulate_stacked_dataframe(
            BIN_GRID_2D, THETA_TRUE, A_c_true=A_C_TRUE, noise_scale=0.03,
        )
        result = dem.fit_dust_evolution(sim["df"], fix_a_M=True, **MCMC_STD)
        assert result is not None
        # Within the first z-slice (bins 0, 1, 2), σ_SFR increases
        Tw_rec = result.T_w_grid[:3]
        assert Tw_rec[0] < Tw_rec[2], (
            f"T_w should increase with σ_SFR; got {Tw_rec}"
        )

    def test_mass_dependence_recovered(self, dem):
        """a_M ≠ 0: higher M* → higher f_w — recovered by joint fit on 3D grid.

        Uses a0=-1.5 (not THETA_TRUE's -0.5) so f_w stays in 30-75% range
        across the M* and z grid — both components contribute and the mass
        dependence is clearly visible in the SED shape.
        """
        # a0=-1.5 at z=0.75, M*=9.5: f_w≈33%; at z=2.5, M*=11.2: f_w≈74%
        theta_mass = np.array([30.0, 55.0, 5.0, -1.5, 0.10, 0.1, 0.0])

        sim = dem.simulate_stacked_dataframe(
            BIN_GRID_3D, theta_mass, A_c_true=A_C_TRUE_3D, noise_scale=0.005,
        )
        result = dem.fit_dust_evolution(sim["df"], fix_a_M=False, theta_init=theta_mass, **MCMC_LONG)
        assert result is not None
        # a_M recovered positive (sign check — theta_global is 5 or 6 params)
        a_M_idx = result.param_names.index("a_M")
        assert result.theta_global[a_M_idx] > 0, (
            f"a_M should be positive; got {result.theta_global[a_M_idx]:.3f}"
        )

    def test_zero_mass_dependence(self, dem):
        """On a 3D grid with true a_M=0, fitter should not pull a_M away from 0."""
        sim = dem.simulate_stacked_dataframe(
            BIN_GRID_3D, THETA_TRUE, A_c_true=A_C_TRUE_3D, noise_scale=0.03,
        )
        result = dem.fit_dust_evolution(sim["df"], fix_a_M=False, **MCMC_LONG)
        assert result is not None
        a_M_idx = result.param_names.index("a_M")
        a_M_rec = result.theta_global[a_M_idx]
        # Should not be strongly biased away from 0
        assert abs(a_M_rec) < 0.3, (
            f"a_M should be near 0; got {a_M_rec:.3f} — possible M*-σ_SFR degeneracy"
        )
